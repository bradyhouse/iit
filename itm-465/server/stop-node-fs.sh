_pid=$(lsof -i:8888 -t || echo '-1')  # Capture Process Id

#try
(
  # Verify pid found
  if [[ ${_pid} -eq "-1" ]]; then exit 0; fi

  # Kill the process
  $(lsof -i:8888 -t | xargs kill;) || exit 86

)
#catch
_rc=$?
case ${_rc} in
    0)  echo "http://localhost:8888 ~ is dead."
        ;;
    86) echo "FUBAR ~ Attempt to kill Pid = ${_pid} failed."
        ;;
    *)  echo "An unknown error has occurred. Haaa-Ha! You Win :)"
        ;;
esac
#bubble up (re-throw)
exit ${_rc}

